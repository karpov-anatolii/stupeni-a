      <div><strong>Bowstring staircase </strong> - the side part of the stairs is a reinforced concrete stringer beam with a rectangular cross-section, from which the steps emerge. The bowstring is the main supporting element of the stairs. Bowstring stairs can be both smooth lined and with open steps. </div>

      <div>&nbsp;</div>

      <div><strong>Open riser </strong> - a flight of stairs consists of separate steps (treads) isolated from each other. The steps have the form of equal plates with an average thickness of 80-110 mm, which come out of the bowstring or stringer staircases. </div>

      <div>&nbsp;</div>

      <div><strong>Stringer </strong> - Straight or bent beam on which the steps lie. There are options with two beams, which are the main load-bearing elements of the stairs. Mainly treads with open steps are placed on the stringer, but you can also lay a smoothly poured march. </div>

      <div>&nbsp;</div>

      <div><strong>Tread</strong> is the horizontal plane of the step on which the foot steps. The tread width is on average from 250 to 350 mm. </div>

      <div>&nbsp;</div>

      <div><strong>Riser</strong> - the vertical plane of the step, which sets the height of the step. The height of the step varies from 150 to 200 mm.</div>

      <div>&nbsp;</div>

      <div><strong>Straight staircase </strong> - on the plan (view from above), the flight of stairs has the shape of an elongated rectangle. The middle line of the staircase is straight.</div>

      <div>&nbsp;</div>

      <div><strong>L-shaped staircase </strong> - on the plan, the flight of stairs has the shape of the letter &quot; L &quot;. There are options with an intermediate platform or with running steps. </div>

      <div>&nbsp;</div>

      <div><strong>U-shaped staircase </strong> - in plan the letter &quot; U &quot;. Four options: with one cross-walk platform, with 2 cross-walk platforms, with running steps and combined. </div>

      <div>&nbsp;</div>

      <div><strong>Spiral staircase </strong> - on the plan is a flight of stairs in the shape of a circle. The middle line of the march is a circle. All steps have the same inner and outer radius. The turn of a spiral staircase can be complete (360 degrees) or incomplete (less than 360 degrees). At the top, the stairs end with a cantilever platform, which can be supported by a spiral staircase going to the next floor. </div>

      <div>&nbsp;</div>

      <div><strong>Bent staircase </strong> - a flight of stairs bent in plan with an arbitrary curve. All steps do not have a common center of inner and outer radius (as in spiral staircases). They can be both wall-mounted and free-standing staircases. </div>

      <div>&nbsp;</div>

