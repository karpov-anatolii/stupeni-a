      <div><strong>Smoothly poured staircase </strong> - from below, the stairs have a flat or twisted curved surface, without voids (as in the case of open steps). The smoothness of the lower surface of the stairs is achieved by precise fitting of formwork plywood sheets-segments. The stairs are very strong, but heavy. </div>

